/*
This program prints a face, using text characters!
*/
public class ExerciseP2
{
    public static void main(String[] args)
    {
        System.out.println("  /////  ");  // this is the hair
        System.out.println(" | o o | ");  // these are the eyes
        System.out.println("(|  ^  |)");  // these are the ears and nose
        System.out.println(" | \\_/ | "); // this is the mouth smiling
        System.out.println("  -----  ");  // this is the chin, bottom of face
    }
}